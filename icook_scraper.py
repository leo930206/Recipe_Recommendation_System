# Êñ∞Â¢ûÔºöremove_illegal_characters
# ÂäüËÉΩÔºöÁßªÈô§ÁÑ°Ê≥ïÂØ´ÂÖ• Excel ÁöÑÈùûÊ≥ïÂ≠óÁ¨¶
# ---------------------------------------------------------------------
def remove_illegal_characters(text):
    """ÁßªÈô§‰∏çÂêàÊ≥ïÂ≠óÁ¨¶ÔºåÂè™‰øùÁïô‰∏≠ÊñáÂ≠óÁ¨¶ÂíåÂÖ∂‰ªñÊ≠£Â∏∏Â≠óÁ¨¶"""
    if text:
        # ÁßªÈô§ÊâÄÊúâÈùûASCIIÁØÑÂúçÁöÑÂ≠óÁ¨¶Ôºà‰∏çÂåÖÊã¨‰∏≠ÊñáÂ≠óÁ¨¶Ôºâ
        # ‰øùÁïô‰∏≠ÊñáÂ≠óÁ¨¶Âíå‰∏ÄËà¨ÁöÑÂ≠óÊØç„ÄÅÊï∏Â≠ó„ÄÅÊ®ôÈªûÁ¨¶Ëôü
        text = re.sub(r'[^\x00-\x7F\u4e00-\u9fff]+', '', text)
        # ÁßªÈô§ÊéßÂà∂Â≠óÁ¨¶Ôºà‰æãÂ¶ÇExcel‰∏çÊé•ÂèóÁöÑÂ≠óÁ¨¶Ôºâ
        text = re.sub(r'[\x01-\x1F\x7F]', '', text)
    return text


