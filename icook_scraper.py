# Êñ∞Â¢ûÔºösave_recipes_to_excel
# ÂäüËÉΩÔºöÂ∞áÊî∂ÈõÜÂà∞ÁöÑÊâÄÊúâÈ£üË≠úË≥áÊñôÂ≠òÊàê Excel Ê™î (.xlsx)„ÄÇ
# ---------------------------------------------------------------------
def save_recipes_to_excel(recipes, filename='recipes.xlsx'):
    """
    Â∞áÈ£üË≠úÊï∏Êìö‰øùÂ≠òÁÇ∫ Excel Êñá‰ª∂„ÄÇ
    ÈúÄË¶ÅÂÆâË£ù pandas Ëàá openpyxl (Êàñ xlsxwriter)„ÄÇ
    """
    data_for_df = []
    for r in recipes:
        data_for_df.append({
            'È£üÁâ©': remove_illegal_characters(r['È£üÁâ©']),
            'Ê®ôÁ±§': ', '.join([remove_illegal_characters(tag) for tag in r['Ê®ôÁ±§']]) if r['Ê®ôÁ±§'] else "",
            'È£üÊùê': '\n'.join([remove_illegal_characters(ingredient) for ingredient in r['È£üÊùê']]) if r['È£üÊùê'] else "",
            'Ê≠•È©ü': '\n'.join([remove_illegal_characters(step) for step in r['Ê≠•È©ü']]) if r['Ê≠•È©ü'] else "",
            '‰ªΩÈáè': remove_illegal_characters(r['‰ªΩÈáè']),
            'ÊôÇÈñì': remove_illegal_characters(r['ÊôÇÈñì'])
        })

    df = pd.DataFrame(data_for_df, columns=['È£üÁâ©', 'Ê®ôÁ±§', 'È£üÊùê', 'Ê≠•È©ü', '‰ªΩÈáè', 'ÊôÇÈñì'])
    df.to_excel(filename, index=False)
    print(f"ÊâÄÊúâÈ£üË≠úÊï∏ÊìöÂ∑≤‰øùÂ≠òÂà∞ {filename}")

